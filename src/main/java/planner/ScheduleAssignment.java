package planner;

import org.optaplanner.core.api.domain.solution.PlanningEntityCollectionProperty;
import org.optaplanner.core.api.domain.solution.PlanningSolution;
import org.optaplanner.core.api.domain.solution.Solution;
import org.optaplanner.core.api.domain.valuerange.ValueRangeProvider;
import org.optaplanner.core.api.score.buildin.hardsoft.HardSoftScore;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

/**
 * Created by takako.shimamoto on 15/11/05.
 */
@PlanningSolution
public class ScheduleAssignment implements Solution<HardSoftScore> {

    private List<CronSchedule> schedules;
    private List<JobProcess> jobs;

    private HardSoftScore score;

    @ValueRangeProvider(id = "scheduleRange")
    public List<CronSchedule> getSchedules() {
        return schedules;
    }

    public void setSchedules(List<CronSchedule> schedules) {
        this.schedules = schedules;
    }

    @PlanningEntityCollectionProperty
    public List<JobProcess> getJobs() {
        return jobs;
    }

    public void setJobs(List<JobProcess> jobs) {
        this.jobs = jobs;
    }

    @Override
    public HardSoftScore getScore() {
        return score;
    }

    @Override
    public void setScore(HardSoftScore score) {
        this.score = score;
    }

    @Override
    public Collection<? extends Object> getProblemFacts() {
        List<Object> facts = new ArrayList<>();
        facts.addAll(schedules);
        // Do not add the planning entity's (jobs) because that will be done automatically
        return facts;
    }
}
